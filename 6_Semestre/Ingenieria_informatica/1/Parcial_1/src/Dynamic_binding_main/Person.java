package Dynamic_binding_main;

public class Person 
{
	private String name;
	private String adress;
	private String phoneNumber;
	private String email;
	
	Person(String name, String adress, String phoneNumber, String email)
	{
		this.name = name;
		this.adress = adress;
		this.phoneNumber = phoneNumber;
		this.email = email;
	}
}
